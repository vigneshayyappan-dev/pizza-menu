function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1>Fast React Pizza Co.</h1>
    </header>
  );
}

function Menu() {
  return (
    <main className="menu">
      <h2>Our Menu</h2>
      <Pizza
        name={pizzaData[0].name}
        ingredients={pizzaData[0].ingredients}
        photoName={pizzaData[0].photoName}
        price={pizzaData[0].price}
      />
    </main>
  );
}

function Pizza(props) {
  return (
    <div>
      <img src={props.photoName} alt={props.name} />
      <h3>{props.name}</h3>
      <p>{props.ingredients}</p>
      <span>{props.price + 5}</span>
    </div>
  );
}
function Footer() {
  const hour = new Date().getHours();
  const openHour = 12;
  const closeHour = 22;
  let textToShow;
  if (hour >= openHour && hour <= closeHour)
    textToShow = "We're currently open!";
  else textToShow = "Sorry we're closed";

  return (
    <div className="footer">
      <footer>
        {new Date().toLocaleTimeString()}. {textToShow}
      </footer>
    </div>
  );
}

const root = ReactDOM.createRoot(document.getElementById("root")); // export default App;
